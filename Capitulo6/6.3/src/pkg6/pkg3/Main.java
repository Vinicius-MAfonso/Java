package pkg6.pkg3;

public class Main {

    public static void main(String[] args) {
      System.out.println("O valor absoluto de 23.7 é: "+Math.abs(27.7));
      System.out.println("O arredondamento de 9.2 é de: "+Math.ceil(9.2));
      System.out.println("O seno de 1.0 é de: "+Math.sin(1.0));
      System.out.println("O cosseno de 1.0 é de: "+Math.cos(1.0));
      System.out.println("A tangente de 1.0 é de: "+Math.tan(1.0));
      System.out.println("O exponencial de 2.0 é de: "+Math.exp(2.0));
      System.out.println("O maior valor entre 2 e 5 é: "+Math.max(2.0,5.0));
      System.out.println("O menor valor entre 2 e 5 é: "+Math.min(2.0,5.0));
      System.out.println("A potência de 2 e 2: "+Math.pow(2.0,2.0));
      System.out.println("A raiz quadrada de 4 é: "+Math.sqrt(4.0));
      
      
      
    }
    
}
